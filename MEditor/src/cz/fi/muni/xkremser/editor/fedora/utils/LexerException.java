public class LexerException extends Exception {

	/**
	 * 
	 */
	public LexerException() {
		super();

	}

	/**
	 * @param message
	 */
	public LexerException(String message) {
		super(message);

	}

	/**
	 * @param message
	 * @param cause
	 */
	public LexerException(String message, Throwable cause) {
		super(message, cause);

	}

	/**
	 * @param cause
	 */
	public LexerException(Throwable cause) {
		super(cause);

	}
}